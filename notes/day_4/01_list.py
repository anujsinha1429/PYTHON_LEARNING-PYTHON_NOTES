# list are the container to store a set of values of any data types...

friends=["apple","orange",5,345.65,False,"akash","rohan"]

print(friends[0])
# output : apple

# unlike strings lists are mutable, we can change the value of the list
# for exmaple,
friends[0]="lion"
print(friends)
# output will be : ['lion', 'orange', 5, 345.65, False, 'akash', 'rohan']

# list can be indexed just like a string